<h4 id="pm-help-h4">Catégorie</h4>
    <p id="pm-help-p">
        <b>Une ressource est classée dans une catégorie</b>. Les catégories sont 
        utilisés pour les <a href="bookinghelp">habilitation</a> des personnes a 
        utiliser une certaine catégorie de resources.
    </p>
    <p id="pm-help-p">
        <i>Exemple:</i> Si les ressources R1, R2 et R3 sont dans la catégorie C1, 
        alors une personne habilité à utiliser les resources de catégorie C1, poura
        réserver et donc utiliser les resources R1, R2, et R3. 
    </p>
    <p id="pm-help-p">
        Le système de catégories permet donc de diminuer le nombre d'habilitations 
        à éditer lorsqu'une plate-forme possède des équipements similaires.
    </p>

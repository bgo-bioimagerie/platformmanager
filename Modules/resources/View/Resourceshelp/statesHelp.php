<h4 id="pm-help-h4">Etats</h4>
    <p id="pm-help-p">
        <b>Des alertes peuvent etre automatiquement envoyées</b> aux responsables 
        des ressources et/ou aux utilisateurs.  
    </p>
    <p id="pm-help-p">
        Pour cela, il faut définir quels status sont lanceurs d'alertes et quand l'alerte doit etre lancée.
    </p>
    <p id="pm-help-p">
        <i>Exemple d'alertes possible:</i>
    <ul style="color: #666;">
        <li>
            Prévenir les responsable de la ressource lorsque qu'un évenement avec le status "métrologie"
            n'as pas eu lieu depuis 2 mois
        </li>
        <li>
            Prévenir tous les utilisateurs lorsque l'évenement avec le status "En panne" viens d'etre ajouté
        </li>
    </ul>
</p>

